export class Customer{
    constructor(id, name, dni, ruc, phoneNumber, email, address, customerType){
        this.id = id;
        this.name = name;
        this.dni = dni;
        this.ruc = ruc;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.address = address;
        this.customerType = customerType;
    }
}